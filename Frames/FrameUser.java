public void toPrint(){
    
    ReportClass aux=report.Report.List.inicio;
    list.clear();
   
   if(aux.sig==report.Report.List.inicio){
       list.addElement("Titulo"+aux.getNombre()+" Estado: "+aux.getEstado()+" Id Repote: "+aux.getId());
      
       List.setModel(list); 
   }else{
       
       
       do{
           list.addElement("Titulo"+aux.getNombre()+" Estado: "+aux.getEstado()+" Id Repote: "+aux.getId()+" Respuesta"+aux.getObervacionAdministrador());
           aux=aux.sig;
       
       } while(aux!=report.Report.List.inicio);
    
    List.setModel(list);   //Manda lo que este en el list model al jlist
}
}
