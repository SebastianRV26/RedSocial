    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UserMethods user = UserMethods.getInstance();
        String userName = jTextField1.getText();
        String password = jTextField2.getText();
        String country = jTextField3.getText();
        String city = jTextField4.getText();
        int identification = Integer.parseInt(jTextField5.getText());
        String name = jTextField6.getText();
        String surname = jTextField7.getText();
        String surname2 = jTextField8.getText();
        String photography = jTextField9.getText();
        
        if(user.insertarInicioCircular(userName, password,country, city, identification, name, surname, surname2, photography)){
            JOptionPane.showMessageDialog(null,"Registrado"); 
            LogIn f4 = new LogIn();
            f4.setVisible(true);
            dispose();
        }
        else
            JOptionPane.showMessageDialog(null,"Espacios en blanco"); 
    }//GEN-LAST:event_jButton1ActionPerformed

