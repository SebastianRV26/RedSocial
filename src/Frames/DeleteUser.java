   public void allUser(){
        User aux = met.inicio;
        list.clear();
        while (aux!=met.fin){
            list.addElement(aux.name);
            list.addElement(""+aux.identification);
            aux = aux.sig;
        }
        list.addElement(aux.name);
        list.addElement(""+aux.identification);
        jList1.setModel(list);
   }
   
   public void userSend(int id){
       ReportClass nuevo=Main.List.inicio;
       if(nuevo.sig==Main.List.inicio){
           if(nuevo.getIdcard()==id){
               nuevo.getState("Realizado");
               return;
           }
            
            
        }else{
            do{
               if(nuevo.getIdcard()==id){
               nuevo.getState("Realizado");
               
               }
                
            } while(nuevo!=Main.List.inicio);
            return;
        } 
   }
